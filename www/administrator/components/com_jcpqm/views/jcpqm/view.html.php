/**
 * Jcpqm View class
 */
class JcpqmViewJcpqm extends JViewLegacy
{
	/**
	 * View display method
	 * @return void
	 */
	function display($tpl = null)
	{
		// Assign data to the view
		$this->icons			= $this->get('Icons');
		$this->contributors		= JcpqmHelper::getContributors();
		
		// get the manifest details of the component
		$this->manifest = JcpqmHelper::manifest();
		
		// Set the toolbar
		$this->addToolBar();
		
		// Check for errors.
		if (count($errors = $this->get('Errors')))
		{
			throw new Exception(implode("\n", $errors), 500);
		}

		// Display the template
		parent::display($tpl);

		// Set the document
		$this->setDocument();
	}

	/**
	 * Setting the toolbar
	 */
	protected function addToolBar()
	{
		$canDo = JcpqmHelper::getActions('jcpqm');
		JToolBarHelper::title(JText::_('COM_JCPQM_DASHBOARD'), 'grid-2');

		// set help url for this view if found
		$help_url = JcpqmHelper::getHelpUrl('jcpqm');
		if (JcpqmHelper::checkString($help_url))
		{
			JToolbarHelper::help('COM_JCPQM_HELP_MANAGER', false, $help_url);
		}

		if ($canDo->get('core.admin') || $canDo->get('core.options'))
		{
			JToolBarHelper::preferences('com_jcpqm');
		}
	}

	/**
	 * Method to set up the document properties
	 *
	 * @return void
	 */
	protected function setDocument()
	{
		$document = JFactory::getDocument();
		
		// add dashboard style sheets
		$document->addStyleSheet(JURI::root() . "administrator/components/com_jcpqm/assets/css/dashboard.css");
		
		// set page title
		$document->setTitle(JText::_('COM_JCPQM_DASHBOARD'));
		
		// add manifest to page JavaScript
		$document->addScriptDeclaration("var manifest = jQuery.parseJSON('" . json_encode($this->manifest) . "');", "text/javascript");
	}
}
